
<template>
  <v-app>
    <v-app-bar color="primary" dark>
      <v-btn icon @click="goBack">
        <v-icon>mdi-arrow-left</v-icon>
      </v-btn>
      <v-app-bar-title>User Management</v-app-bar-title>
      <v-spacer></v-spacer>
      <v-btn icon @click="logout">
        <v-icon>mdi-logout</v-icon>
      </v-btn>
    </v-app-bar>

    <v-navigation-drawer v-model="drawer" app>
      <v-list dense>
        <v-list-item prepend-icon="mdi-folder" title="Projects" to="/projects"></v-list-item>
        <v-list-item prepend-icon="mdi-account-group" title="Users" to="/users"></v-list-item>
      </v-list>
      <template v-slot:append>
        <div class="pa-2">
          <v-chip>{{ user?.name }}</v-chip>
          <v-chip color="secondary" class="ml-2">{{ user?.role }}</v-chip>
        </div>
      </template>
    </v-navigation-drawer>

    <v-main>
      <v-container>
        <v-row>
          <v-col cols="12">
            <div class="d-flex justify-space-between align-center mb-4">
              <h1 class="text-h4">Users</h1>
              <v-btn v-if="canCreateUsers" color="primary" @click="showCreateDialog = true">
                <v-icon left>mdi-plus</v-icon>
                New User
              </v-btn>
            </div>
          </v-col>
        </v-row>

        <v-row v-if="loading">
          <v-col cols="12" class="text-center">
            <v-progress-circular indeterminate color="primary"></v-progress-circular>
          </v-col>
        </v-row>

        <v-row v-else-if="users.length === 0">
          <v-col cols="12">
            <v-alert type="info">No users found.</v-alert>
          </v-col>
        </v-row>

        <v-row v-else>
          <v-col cols="12">
            <v-card>
              <v-data-table
                :headers="headers"
                :items="users"
                :items-per-page="10"
              >
                <template v-slot:item.name="{ item }">
                  <div class="d-flex align-center">
                    <v-avatar color="primary" size="32" class="mr-2">
                      <span class="white--text">{{ item.name?.charAt(0).toUpperCase() }}</span>
                    </v-avatar>
                    {{ item.name }}
                  </div>
                </template>
                
                <template v-slot:item.role="{ item }">
                  <v-chip :color="getRoleColor(item.role)" size="small">
                    {{ getRoleName(item.role) }}
                  </v-chip>
                </template>
                
                <template v-slot:item.created_at="{ item }">
                  {{ formatDate(item.created_at) }}
                </template>
                
                <template v-slot:item.actions="{ item }">
                  <v-btn icon size="small" @click="editUser(item)">
                    <v-icon>mdi-pencil</v-icon>
                  </v-btn>
                  <v-btn 
                    v-if="canDeleteUsers" 
                    icon 
                    size="small" 
                    color="error"
                    @click="confirmDelete(item)"
                    :disabled="item.id === user?.id"
                  >
                    <v-icon>mdi-delete</v-icon>
                  </v-btn>
                </template>
              </v-data-table>
            </v-card>
          </v-col>
        </v-row>

        <!-- Create/Edit Dialog -->
        <v-dialog v-model="showCreateDialog" max-width="600">
          <v-card>
            <v-card-title>{{ editingUser ? 'Edit User' : 'New User' }}</v-card-title>
            <v-card-text>
              <v-form ref="form" v-model="formValid">
                <v-text-field
                  v-model="userForm.name"
                  label="Name"
                  required
                  :rules="[v => !!v || 'Name is required']"
                ></v-text-field>
                <v-text-field
                  v-model="userForm.email"
                  label="Email"
                  type="email"
                  required
                  :rules="[v => !!v || 'Email is required', v => /.+@.+\..+/.test(v) || 'Email must be valid']"
                ></v-text-field>
                <v-select
                  v-model="userForm.role"
                  label="Role"
                  :items="roleOptions"
                  :disabled="!canChangeRole"
                  :rules="[v => !!v || 'Role is required']"
                ></v-select>
                <v-text-field
                  v-model="userForm.password"
                  label="Password"
                  type="password"
                  :hint="editingUser ? 'Leave blank to keep current password' : 'Required'"
                  :persistent-hint="editingUser"
                  :rules="editingUser ? [] : [v => !!v || 'Password is required']"
                ></v-text-field>
                <v-text-field
                  v-model="userForm.password_confirmation"
                  label="Confirm Password"
                  type="password"
                  :rules="[v => v === userForm.password || 'Passwords must match']"
                ></v-text-field>
              </v-form>
              <v-alert v-if="errorMessage" type="error" class="mt-2">{{ errorMessage }}</v-alert>
            </v-card-text>
            <v-card-actions>
              <v-spacer></v-spacer>
              <v-btn @click="closeDialog">Cancel</v-btn>
              <v-btn color="primary" @click="saveUser" :disabled="!formValid">
                {{ editingUser ? 'Update' : 'Create' }}
              </v-btn>
            </v-card-actions>
          </v-card>
        </v-dialog>

        <!-- Delete Confirmation Dialog -->
        <v-dialog v-model="showDeleteDialog" max-width="400">
          <v-card>
            <v-card-title>Confirm Delete</v-card-title>
            <v-card-text>
              Are you sure you want to delete user "{{ userToDelete?.name }}"?
            </v-card-text>
            <v-card-actions>
              <v-spacer></v-spacer>
              <v-btn @click="showDeleteDialog = false">Cancel</v-btn>
              <v-btn color="error" @click="deleteUser">Delete</v-btn>
            </v-card-actions>
          </v-card>
        </v-dialog>
      </v-container>
    </v-main>
  </v-app>
</template>

<script setup>
import { ref, computed, onMounted } from 'vue';
import { useRouter } from 'vue-router';
import { useAuth } from '@/composables/useAuth';
import api from '@/plugins/api';

const router = useRouter();
const { user, isAdmin, isPM, logout: authLogout } = useAuth();

const drawer = ref(true);
const users = ref([]);
const loading = ref(false);
const showCreateDialog = ref(false);
const showDeleteDialog = ref(false);
const editingUser = ref(null);
const userToDelete = ref(null);
const formValid = ref(false);
const errorMessage = ref('');

const userForm = ref({
  name: '',
  email: '',
  role: 'viewer',
  password: '',
  password_confirmation: '',
});

const headers = [
  { title: 'Name', key: 'name' },
  { title: 'Email', key: 'email' },
  { title: 'Role', key: 'role' },
  { title: 'Created', key: 'created_at' },
  { title: 'Actions', key: 'actions', sortable: false },
];

const roleOptions = [
  { title: 'Admin', value: 'admin' },
  { title: 'Project Manager', value: 'pm' },
  { title: 'Engineer', value: 'engineer' },
  { title: 'Viewer', value: 'viewer' },
];

// Permissions
const canCreateUsers = computed(() => isAdmin.value);
const canEditUsers = computed(() => isAdmin.value || isPM.value);
const canDeleteUsers = computed(() => isAdmin.value);
const canChangeRole = computed(() => isAdmin.value);

const fetchUsers = async () => {
  loading.value = true;
  try {
    const response = await api.get('/users');
    users.value = response.data.data || response.data;
  } catch (err) {
    console.error('Failed to fetch users:', err);
  } finally {
    loading.value = false;
  }
};

const getRoleColor = (role) => {
  const colors = {
    admin: 'red',
    pm: 'blue',
    engineer: 'green',
    viewer: 'grey',
  };
  return colors[role] || 'grey';
};

const getRoleName = (role) => {
  const names = {
    admin: 'Admin',
    pm: 'Project Manager',
    engineer: 'Engineer',
    viewer: 'Viewer',
  };
  return names[role] || role;
};

const formatDate = (date) => {
  return new Date(date).toLocaleDateString();
};

const editUser = (user) => {
  errorMessage.value = '';
  editingUser.value = user;
  userForm.value = {
    name: user.name,
    email: user.email,
    role: user.role,
    password: '',
    password_confirmation: '',
  };
  showCreateDialog.value = true;
};

const confirmDelete = (user) => {
  userToDelete.value = user;
  showDeleteDialog.value = true;
};

const closeDialog = () => {
  showCreateDialog.value = false;
  editingUser.value = null;
  errorMessage.value = '';
  userForm.value = {
    name: '',
    email: '',
    role: 'viewer',
    password: '',
    password_confirmation: '',
  };
};

const saveUser = async () => {
  if (!formValid.value) {
    return;
  }
  
  try {
    const data = { ...userForm.value };
    
    // Remove password_confirmation from the data
    delete data.password_confirmation;
    
    // Remove password if empty (for editing)
    if (editingUser.value && !data.password) {
      delete data.password;
    }
    
    if (editingUser.value) {
      await api.put(`/users/${editingUser.value.id}`, data);
    } else {
      await api.post('/users', data);
    }
    closeDialog();
    fetchUsers();
  } catch (err) {
    console.error('Failed to save user:', err.response?.data || err.message);
    errorMessage.value = err.response?.data?.message || err.response?.data?.error || 'Failed to save user';
  }
};

const deleteUser = async () => {
  try {
    await api.delete(`/users/${userToDelete.value.id}`);
    showDeleteDialog.value = false;
    userToDelete.value = null;
    fetchUsers();
  } catch (err) {
    console.error('Failed to delete user:', err);
    alert(err.response?.data?.message || 'Failed to delete user');
  }
};

const goBack = () => router.push('/projects');
const logout = async () => {
  await authLogout();
  router.push('/login');
